dispatcher.add_handler(MessageHandler(Filters.text & ~Filters.command, echo))

# –°–æ–∑–¥–∞–µ–º Flask –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ
app = Flask(__name__)

@app.route('/')
def index():
    return "–ë–æ—Ç —Ä–∞–±–æ—Ç–∞–µ—Ç! üöÄ"

@app.route('/webhook', methods=['POST'])
def webhook():
    """–≠–Ω–¥–ø–æ–∏–Ω—Ç –¥–ª—è –≤–µ–±—Ö—É–∫–∞ –æ—Ç Telegram"""
    if request.method == 'POST':
        update = Update.de_json(request.get_json(force=True), bot)
        dispatcher.process_update(update)
    return 'ok'

@app.route('/set_webhook', methods=['GET'])
def set_webhook():
    """–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –≤–µ–±—Ö—É–∫–∞ (–≤—ã–∑–æ–≤–∏—Ç–µ —ç—Ç–æ—Ç URL –æ–¥–∏–Ω —Ä–∞–∑ –ø–æ—Å–ª–µ –¥–µ–ø–ª–æ—è)"""
    webhook_url = os.environ.get('RENDER_EXTERNAL_URL') + '/webhook'
    
    if not webhook_url or webhook_url == '/webhook':
        return "–û—à–∏–±–∫–∞: RENDER_EXTERNAL_URL –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω"
    
    success = bot.set_webhook(webhook_url)
    if success:
        return f"–í–µ–±—Ö—É–∫ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω: {webhook_url}"
    else:
        return "–û—à–∏–±–∫–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –≤–µ–±—Ö—É–∫–∞"

